function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function isPlainObject(value) {
  return value !== null && typeof value === 'object' && !Array.isArray(value);
}

function isTranslationLeaf(value) {
  if (!isPlainObject(value)) {
    return true;
  }
  return typeof value.text === 'string' || typeof value.html === 'string';
}

function flattenTranslations(value, prefix = '', output = {}) {
  if (!prefix && !isPlainObject(value)) {
    return output;
  }
  if (prefix && isTranslationLeaf(value)) {
    output[prefix] = value;
    return output;
  }
  Object.entries(value || {}).forEach(([key, child]) => {
    flattenTranslations(child, prefix ? `${prefix}.${key}` : key, output);
  });
  return output;
}

function shouldSkipKey(key) {
  return SKIP_PREFIXES.some((prefix) => key === prefix || key.startsWith(prefix));
}

function translateString(key, value) {
  if (Object.prototype.hasOwnProperty.call(KEY_PHRASE_OVERRIDES, key)) {
    return KEY_PHRASE_OVERRIDES[key];
  }
  if (Object.prototype.hasOwnProperty.call(PHRASE_TRANSLATIONS, value)) {
    return PHRASE_TRANSLATIONS[value];
  }
  if (/^{{field}} must be 0 or more\.$/.test(value)) {
    return '{{field}} חייב להיות 0 או יותר.';
  }
  if (/^{{field}} is required\.$/.test(value)) {
    return 'השדה {{field}} נדרש.';
  }
  if (/^{{rate}} × {{days}} {{daysLabel}} = {{total}}$/.test(value)) {
    return '{{rate}} × {{days}} {{daysLabel}} = {{total}}';
  }
  if (/^3-day package: {{total}}$/.test(value)) {
    return 'חבילת 3 ימים: {{total}}';
  }
  if (/^Total rental price: {{price}}$/.test(value)) {
    return 'סה״כ מחיר השכרה: {{price}}';
  }
  if (/^Total rental price: {{total}} {{currency}} \({{days}} {{daysLabel}}\)$/.test(value)) {
    return 'סה״כ מחיר השכרה: {{total}} {{currency}} ({{days}} {{daysLabel}})';
  }
  if (/^Total rental price \({{days}} {{daysLabel}}\)$/.test(value)) {
    return 'סה״כ מחיר השכרה ({{days}} {{daysLabel}})';
  }
  if (/^Young driver: {{pricePerDay}} × {{days}} {{daysLabel}} = {{total}}$/.test(value)) {
    return 'נהג צעיר: {{pricePerDay}} × {{days}} {{daysLabel}} = {{total}}';
  }
  if (/^{{count}} seats$/.test(value)) {
    return '{{count}} מושבים';
  }
  if (/^{{price}} \/ day$/.test(value)) {
    return '{{price}} ליום';
  }
  if (/^\/ day$/.test(value)) {
    return '/ יום';
  }
  if (/^\/ hour$/.test(value)) {
    return '/ שעה';
  }
  if (/^Total luggage exceeds route limit \({{max}}\)\.$/.test(value)) {
    return 'סך המטען חורג ממגבלת המסלול ({{max}}).';
  }
  return null;
}

function translateValue(key, value) {
  if (Object.prototype.hasOwnProperty.call(KEY_TRANSLATIONS, key)) {
    return KEY_TRANSLATIONS[key];
  }
  if (typeof value === 'string') {
    return translateString(key, value);
  }
  if (Array.isArray(value)) {
    const translated = value.map((entry) => {
      if (typeof entry !== 'string') return entry;
      return PHRASE_TRANSLATIONS[entry] || null;
    });
    return translated.every(Boolean) ? translated : null;
  }
  if (value && typeof value === 'object') {
    if (typeof value.text === 'string') {
      const text = translateString(key, value.text);
      if (!text) return null;
      return { ...value, text };
    }
    if (typeof value.html === 'string') {
      const html = translateString(key, value.html);
      if (!html) return null;
      return { ...value, html };
    }
  }
  return null;
}

function buildP0Keys(readiness) {
  const keys = new Set();
  P0_GROUPS.forEach((groupId) => {
    readiness.missingKeyGroups[groupId]?.keys?.forEach((key) => keys.add(key));
    readiness.sameAsEnglishGroups[groupId]?.keys?.forEach((key) => keys.add(key));
  });
  return [...keys].sort((a, b) => a.localeCompare(b, 'en'));
}

function sortObject(value) {
  return Object.fromEntries(
    Object.entries(value).sort(([left], [right]) => left.localeCompare(right, 'en')),
  );
}

