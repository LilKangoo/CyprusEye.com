const skipped = [];
const unresolved = [];
const applied = [];
let existingReviewed = 0;

p0Keys.forEach((key) => {
  const sourceValue = enFlat[key];
  if (typeof sourceValue === 'undefined') {
    unresolved.push({ key, reason: 'missing from EN flat map' });
    return;
  }
  if (shouldSkipKey(key)) {
    skipped.push({ key, sourceValue });
    return;
  }
  const translatedValue = translateValue(key, sourceValue);
  if (translatedValue === null) {
    unresolved.push({ key, sourceValue });
    return;
  }
  const currentSerialized = JSON.stringify(he[key]);
  const nextSerialized = JSON.stringify(translatedValue);
  if (currentSerialized === nextSerialized) {
    existingReviewed += 1;
    return;
  }
  he[key] = translatedValue;
  applied.push({ key, sourceValue, translatedValue });
});

